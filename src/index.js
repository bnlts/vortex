function tryLogin() {
  if (!ConfigMgr.isConfigured()) return;
  client.login(ConfigMgr.getToken()).catch(err => {
    logger.error(`Connexion impossible: ${err.message}`);
  });
}

global.tryLogin = tryLogin;
