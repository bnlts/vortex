const { exec } = require('child_process');
const port = ConfigMgr.get().dashboard?.port || 3000;
const url  = `http://localhost:${port}`;

setTimeout(() => {
  const cmd = process.platform === 'win32'
    ? `start ${url}`
    : process.platform === 'darwin'
      ? `open ${url}`
      : `xdg-open ${url}`;
  exec(cmd, () => {});
}, 1500);

